from dataclasses import dataclass


@dataclass(frozen=True, kw_only=True)
class NormalizedCategory:
    slug: str
    name: str
    parent_slug: str | None = None
    keywords: tuple[str, ...] = ()


def category(
    slug: str,
    name: str,
    *,
    parent_slug: str | None = None,
    keywords: tuple[str, ...] = (),
) -> NormalizedCategory:
    return NormalizedCategory(
        slug=slug,
        name=name,
        parent_slug=parent_slug,
        keywords=keywords,
    )


