def get_normalized_category(slug: str) -> NormalizedCategory | None:
    for normalized_category in DEFAULT_NORMALIZED_CATEGORIES:
        if normalized_category.slug == slug:
            return normalized_category
    return None


def iter_root_categories() -> tuple[NormalizedCategory, ...]:
    return tuple(
        normalized_category
        for normalized_category in DEFAULT_NORMALIZED_CATEGORIES
        if normalized_category.parent_slug is None
    )


def iter_leaf_categories() -> tuple[NormalizedCategory, ...]:
    return tuple(
        normalized_category
        for normalized_category in DEFAULT_NORMALIZED_CATEGORIES
        if normalized_category.keywords
    )
